            errorMessage = "لا يمكن استخدام الرموز أو الحروف العربية في الرابط";
        }else{
            errorMessage = "لا يجب أن يحتوي الرابط على فراغات";
        }
    }
    $("#name").focus().addClass("err-inpt");
  }

  if (errorStep) {
    currentStep = errorStep;
    $(".main-err").removeClass("d-none");
    $(".err-show").text(errorMessage);
    showStep(currentStep);
    return;
  }

  const aftValue = new URLSearchParams(window.location.search).get('aft');
  if (aftValue) {
    formData.append('aft', aftValue);
  }

  $('#multiStepForm').find('input[type!="file"], select, textarea').each(function() {
    formData.append(this.name, $(this).val());
  });

  $('#multiStepForm').find('input[type="file"]').each(function() {
    if (this.files && this.files.length > 0) {
      for (let i = 0; i < this.files.length; i++) {
        formData.append(this.name, this.files[i]);
      }
    }
  });

  console.log($('.theme-select:checked').val());
  var eltheme = $('.theme-select:checked').val();
  formData.append('theme', eltheme);

  $.ajax({
    url: "{{ route('register.store') }}",
    headers: { "X-CSRF-TOKEN": $('meta[name="csrf-token"]').attr("content") },
    data: formData,
    contentType: false,
    processData: false,
    type: "POST",
    beforeSend: function() {
      currentStep = 2;
      showStep(currentStep);
    },
    success: function(response) {
        console.log(response);
        
      if (response.flag === "error") {
        currentStep = 1;

        $(".main-err").removeClass("d-none");
        $(".err-show").text(response.msg);
         showStep(currentStep); // Show the correct step *after* setting the message
      } else if (response.flag === "success") {
        currentStep = 3;
         showStep(currentStep); // Moved showStep here
        $(".go-home").attr("href", response.msg);
        // window.location.href = "{{route('index')}}";
      }
    },
    error: function(error) {
      currentStep = 1;
      $(".main-err").removeClass("d-none");
      $(".err-show").text("حدث خطأ ما جرب مرة أخرى");
       showStep(currentStep); // And here
    }

  });
});
